#include <iostream>

int main() {
    int m, n;

    // Введите порядковый номер месяца и число
    std::cout << "Введите порядковый номер месяца (1-12): ";
    std::cin >> m;

    std::cout << "Введите число (1-31): ";
    std::cin >> n;

    // Проверим, что m и n находятся в разумных пределах
    if (m < 1 || m > 12 || n < 1 || n > 31) {
        std::cout << "Введенные данные некорректны." << std::endl;
        return 1;
    }

    // Вычислим дату предыдущего дня
    if (n > 1) {
        n--;
    } else {
        if (m == 1) {
            m = 12;
        } else {
            m--;
        }
        n = 31; // Переход к последнему числу предыдущего месяца
    }

    // Выведем результат
    std::cout << "Дата предыдущего дня: " << m << " " << n << std::endl;

    return 0;
}